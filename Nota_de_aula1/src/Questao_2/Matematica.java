package Questao_2;

public class Matematica {
    int n1,n2,total;

    public void soma(){
        total = n1+n2;
        System.out.println(total);
    }

    public void sub(){
        total = n1 - n2;
        System.out.println(total);
    }

    public void mult(){
        total = n1*n2;
        System.out.println(total);
    }

    public void div(){
        total = n1/n2;
        System.out.println(total);
    }
}
